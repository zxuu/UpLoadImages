package com.zxu.linkmysql.bean;

public class Urine {
    private String urineId;
    private String time;
    private String cateUrine;

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getCateUrine() {
        return cateUrine;
    }

    public void setCateUrine(String cateUrine) {
        this.cateUrine = cateUrine;
    }

    public String getUrineId() {
        return urineId;
    }

    public void setUrineId(String urineId) {
        this.urineId = urineId;
    }
}
